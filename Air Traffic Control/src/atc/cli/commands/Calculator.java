package atc.cli.commands;

public class Calculator {

    static public double add(int a, int b) {
        return a + b;
    }

    static public double subtract(int a, int b) {
        return a - b;
    }

    static public double multiply(int a, int b) {
        return a * b;
    }

    static public double divide(int a, int b) {
        return a / b;
    }

    static public double add(int a, int b, int c) {
        return a + b + c;
    }
    
    static public double add(double a, double b) {
        return a + b;
    }

    static public double subtract(double a, double b) {
        return a - b;
    }

    static public double multiply(double a, double b) {
        return a * b;
    }

    static public double divide(double a, double b) {
        return a / b;
    }

    static public double add(double a, double b, double c) {
        return a + b + c;
    }
}
